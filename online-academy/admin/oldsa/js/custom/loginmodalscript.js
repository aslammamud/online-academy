$(document).ready(function(){  
      $('#login_button').click(function(){  
           var email = $('#email').val();  
           var password = $('#password').val();  
           if(email != '' && password != '')  
           {  
                $.ajax({  
                     url:"login.php",
                     method:"POST",  
                     data: {email:email, password:password},  
                     success:function(data)  
                     {  
                          //alert(data);  
                          if(data == 'No')  
                          {  
                               alert("Wrong Data");  
                          }  
                          else  
                          {  
                               $('#loginModal').hide();  
                               location.reload();  
                          }  
                     }  
                });  
           }  
           else  
           {  
                alert("Both Fields are required");  
           }  
      });  
      $('#logout').click(function(){  
           var action = "logout";  
           $.ajax({  
                url:"login.php",  
                method:"POST",  
                data:{action:action},  
                success:function()  
                {  
                     location.reload();  
                }  
           });  
      });  
 });